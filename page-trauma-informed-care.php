                            </tr>
                            <tr>
                                <td>"They're refusing"</td>
                                <td>"They're not ready yet" / "they're choosing a different option today"</td>
                            </tr>
                            <tr>
                                <td>"Treatment resistant"</td>
                                <td>"Treatment hasn't matched needs yet"</td>
                            </tr>
                        </tbody>
                    </table>
                    <p class="table-note"><strong>Rule of thumb:</strong> describe what's happening (observable + clinical) without assigning character flaws.</p>
                </div>
            </section>
            
            <section class="tic-section">
                <h2>How We Counter Stigma and Build Safety at Red Door</h2>
                <p>We treat stigma as a barrier to care—not a moral issue.</p>
                
                <div class="action-box">
                    <h3>We counter stigma by:</h3>
                    <ul>
                        <li>Using person-first, medically accurate language (public site + provider outreach)</li>
                        <li>Highlighting recovery stories and peer pathways (hope + belonging)</li>
                        <li>Making access to basic contact info free so people can reach help without paywalls</li>
                        <li>Encouraging providers to make their pages transparent: what to expect, what it costs, what intake feels like, and how privacy is protected</li>
                    </ul>
                </div>
            </section>
            
            <section class="tic-section">
                <h2>Trauma, ACEs, and Risk Across the Lifespan</h2>
                <p>Adverse Childhood Experiences (ACEs) are potentially traumatic events in childhood and are linked to long-term negative impacts on health and well-being. CDC data also ties prevention of ACEs to reductions in issues like prescription pain medication misuse and other behavioral health outcomes.</p>
                
                <p class="emphasis">This is why "what happened to you?" often belongs in the room alongside "what substances are involved?"—without forcing disclosure.</p>
