            <section class="tic-section">
                <h2>What "Trauma-Informed" Means</h2>
                <p>Trauma-informed care (TIC) is a way of providing services that assumes trauma may be present, and designs every interaction to be safer, more respectful, and less likely to trigger or re-traumatize someone. SAMHSA defines trauma as an event (or series of events/circumstances) experienced as physically or emotionally harmful or life-threatening, with lasting negative effects on functioning and well-being.</p>
                
                <div class="highlight-box">
                    <h3>SAMHSA's "4 R's"</h3>
                    <ul>
                        <li><strong>Realize</strong> the widespread impact of trauma</li>
                        <li><strong>Recognize</strong> the signs and symptoms</li>
                        <li><strong>Respond</strong> by integrating trauma knowledge into policies and practice</li>
                        <li><strong>Resist</strong> re-traumatization</li>
                    </ul>
                </div>
                
                <p>And it's guided by key principles like safety, trustworthiness, peer support, collaboration, empowerment/choice, and cultural, historical, and gender responsiveness.</p>
            </section>
            
            <section class="tic-section">
                <h2>Why Trauma-Informed Care Matters for Substance Use Disorder</h2>
                <p>Trauma and substance use disorder (SUD) commonly overlap. Many people use substances to cope with:</p>
                <ul class="impact-list">
                    <li>Hypervigilance, anxiety, intrusive memories</li>
                    <li>Emotional pain, shame, numbness, insomnia</li>
                    <li>Relationship and attachment injuries</li>
                </ul>
                
                <div class="warning-box">
                    <h3>When care is not trauma-informed, the risks go up:</h3>
                    <p><strong>Dropout and disengagement:</strong> people who feel judged, controlled, or unsafe often stop showing up.</p>
                    <p><strong>Escalation and relapse risk:</strong> coercion, humiliation, or "gotcha" approaches can trigger panic, anger, shutdown, or avoidance—exactly the states that drive use.</p>
                    <p><strong>Re-traumatization:</strong> practices that remove choice, withhold information, or rely on threat/force can recreate dynamics of prior abuse.</p>
                    <p><strong>Worse clinical outcomes:</strong> bias and stigma reduce trust and reduce the odds someone returns for help.</p>
                </div>
                
                <p>SAMHSA specifically notes that practices like seclusion and restraint were once perceived as "therapeutic" in treatment of people with mental and/or substance use disorders—trauma-informed care moves systems away from those harmful dynamics and toward safety and recovery.</p>
            </section>
            
            <section class="tic-section">
                <h2>What Trauma-Informed Care Looks Like (In Real Life)</h2>
                <p class="emphasis">Trauma-informed care isn't "being soft." It's being effective.</p>
                
                <div class="expectations-box">
                    <h3>In a trauma-informed setting, you should expect:</h3>
                    <ul>
                        <li><strong>Transparent communication:</strong> "Here's what we're doing, here's why, and you can ask questions."</li>
                        <li><strong>Choice wherever possible:</strong> options in scheduling, pace, goals, and next steps.</li>
                        <li><strong>Collaborative planning:</strong> "What matters to you?" not "Here's what you must do."</li>
                        <li><strong>Strength-based framing:</strong> focusing on what's working and what's improving.</li>
                        <li><strong>Predictability:</strong> clear boundaries, consistent expectations, and no surprises.</li>
                        <li><strong>Peer support and lived experience:</strong> hope from people who've been there.</li>
                    </ul>
                </div>
            </section>
            
            <section class="tic-section">
                <h2>Language Matters: Person-Centered, Trauma-Informed Phrasing</h2>
                <p>Words can either reduce shame or increase it. Stigmatizing language is linked to negative attitudes and can reduce willingness to seek care.</p>
                
                <div class="language-table-wrapper">
                    <table class="language-table">
                        <thead>
                            <tr>
                                <th>❌ Avoid (stigmatizing/shaming)</th>
                                <th>✅ Use instead (clinical + person-centered)</th>
                            </tr>
                        </thead>
                        <tbody>
                            <tr>
                                <td>"He's an addict."</td>
                                <td>"He's a person with a substance use disorder (SUD)."</td>
                            </tr>
                            <tr>
                                <td>"Drug abuser / substance abuser"</td>
                                <td>"Person with SUD" / "person who uses substances"</td>
                            </tr>
                            <tr>
                                <td>"Dirty" / "failed a drug test"</td>
                                <td>"Positive drug screen" / "tested positive"</td>
                            </tr>
                            <tr>
                                <td>"Clean" / "not clean"</td>
                                <td>"In recovery" / "not currently using" / "in active use"</td>
                            </tr>
                            <tr>
                                <td>"Relapse means they don't want it."</td>
                                <td>"Return to use can be part of recovery; let's adjust the plan."</td>
                            </tr>
                            <tr>
                                <td>"Non-compliant"</td>
                                <td>"Having difficulty with the plan" / "barriers to engagement"</td>
                            </tr>
                            <tr>
                                <td>"Manipulative"</td>
                                <td>"Using survival strategies that worked in the past"</td>
                            </tr>
                            <tr>
